// Synchronous JavaScript example

// Addition
const add = (a, b) => a + b;
const sum = add(5, 3);
console.log("Sum:", sum); // Output: Sum: 8

// Subtraction
const subtract = (a, b) => a - b;
const difference = subtract(10, 6);
console.log("Difference:", difference); // Output: Difference: 4

// Multiplication
const multiply = (a, b) => a * b;
const product = multiply(4, 7);
console.log("Product:", product); // Output: Product: 28

// Division
const divide = (a, b) => a / b;
const quotient = divide(20, 4);
console.log("Quotient:", quotient); // Output: Quotient: 5
